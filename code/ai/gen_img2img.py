_CLAUDE_MASK_PROMPT = (
    "Look at this image: {image_path}\n\n"
    "I want to completely erase EVERY instance of the {object} from this image using AI inpainting.\n"
    "Return ONLY a single line of JSON with no explanation:\n"
    '{{"instances":[{{"x1":<float>,"y1":<float>,"x2":<float>,"y2":<float>}},...], "prompt":"<string>"}}\n'
    "Where:\n"
    "  instances = list of bounding boxes for EVERY visible {object}, each as fractions 0.0-1.0\n"
    "              of the image width/height (x1,y1=top-left, x2,y2=bottom-right)\n"
    "  prompt    = a detailed description of what should appear in place of all erased {object}s.\n"
    "              Be specific: describe the exact background surface (floor, wall, stone, etc.) "
    "that would be visible. Explicitly state there is NO {object} present.\n"
    "If no {object} is visible in the image, return: {{}}"
)


def _extract_first_json(text: str) -> dict | None:
    """Extract and parse the first JSON object from text, handling nested braces."""
    start = text.find('{')
    if start == -1:
        return None
    depth = 0
    for i, ch in enumerate(text[start:], start):
        if ch == '{':
            depth += 1
        elif ch == '}':
            depth -= 1
            if depth == 0:
                try:
                    return json.loads(text[start:i + 1])
                except json.JSONDecodeError:
                    return None
    return None


def _extract_last_json(text: str) -> dict | None:
    """Extract and parse the LAST JSON object from text.
    Claude sometimes emits two JSON blocks (second-guess pattern) — the last one is more reliable."""
    last = None
    pos = 0
    while True:
        start = text.find('{', pos)
        if start == -1:
            break
        depth = 0
        for i, ch in enumerate(text[start:], start):
            if ch == '{':
                depth += 1
            elif ch == '}':
                depth -= 1
                if depth == 0:
                    try:
                        candidate = json.loads(text[start:i + 1])
                        last = candidate
                    except json.JSONDecodeError:
                        pass
                    pos = i + 1
                    break
        else:
            break
    return last


def resolve_mask_and_prompt(args) -> None:
    """
    If args.mask is an existing file path, do nothing.
    If args.mask is an object description (e.g. 'chair', 'people'):
      1. Call claude -p to get bounding box + inpaint prompt from the image.
      2. Generate a white rect mask PNG next to the input image.
      3. Optionally refine with SAM (--sam-refine).
      4. Set args.mask  = path to generated mask PNG.
      5. Set args.prompt = Claude's suggested prompt (if not already set by user).
    """
    if not args.mask:
        return
    if Path(args.mask).exists():
        log.info(f"[mask] Using existing mask file: {args.mask}")
        return

    # --mask is an object name — auto-generate mask via claude -p
    object_name = args.mask
    image_path  = Path(args.input)
    log.info(f"[mask] Auto-generating mask for '{object_name}' in {image_path.name}...")

    prompt = _CLAUDE_MASK_PROMPT.format(
        image_path=str(image_path.resolve()),
        object=object_name,
    )

    try:
        result = subprocess.run(
            [getattr(args, "claude_bin", "claude"), "-p", prompt],
            capture_output=True, text=True, timeout=60,
        )
    except FileNotFoundError:
        log.error("claude CLI not found. Install Claude Code and ensure it is on PATH.")
        sys.exit(1)
    except subprocess.TimeoutExpired:
        log.error("claude -p timed out after 60s.")
        sys.exit(1)

    if result.returncode != 0:
        log.error(f"claude -p exited {result.returncode}: {result.stderr.strip()}")
        sys.exit(1)

    stdout = result.stdout.strip()
    log.info(f"[mask] Claude response: {stdout}")

    data = _extract_first_json(stdout)
    if data is None:
        log.error(f"No valid JSON found in claude response: {stdout}")
        sys.exit(1)

    # Support both legacy single-bbox {x1,y1,x2,y2,...} and new {instances:[...], prompt:...}
    instances = data.get("instances")
    if instances is None:
        # legacy format: treat the whole dict as one bbox
        required = {"x1", "y1", "x2", "y2"}
        if not required.issubset(data.keys()):
            log.error(f"Incomplete bbox in response: {data}")
            sys.exit(1)
        instances = [{k: data[k] for k in required}]

    if not instances:
        log.error(f"Claude could not find '{object_name}' in the image.")
        sys.exit(1)

    # Clamp all bboxes to 0-1
    bboxes = []
    for raw in instances:
        try:
            bboxes.append({k: max(0.0, min(1.0, float(raw[k]))) for k in ("x1","y1","x2","y2")})
        except (KeyError, TypeError, ValueError) as e:
            log.warning(f"[mask] Skipping malformed bbox {raw}: {e}")
    if not bboxes:
        log.error("No valid bounding boxes found.")
        sys.exit(1)

    log.info(f"[mask] {len(bboxes)} instance(s) found")
    for i, b in enumerate(bboxes):
        log.info(f"  [{i}] x1={b['x1']:.3f} y1={b['y1']:.3f} x2={b['x2']:.3f} y2={b['y2']:.3f}")

    # Generate mask — union of all instance rects
    from PIL import Image as PILImage, ImageDraw
    with PILImage.open(str(image_path)) as img:
        img_w, img_h = img.size

    padding = getattr(args, "mask_padding", 20)
    mask_img = PILImage.new("L", (img_w, img_h), 0)
    draw = ImageDraw.Draw(mask_img)
    for bbox in bboxes:
        x1 = max(0,     int(bbox["x1"] * img_w) - padding)
        y1 = max(0,     int(bbox["y1"] * img_h) - padding)
        x2 = min(img_w, int(bbox["x2"] * img_w) + padding)
        y2 = min(img_h, int(bbox["y2"] * img_h) + padding)
        draw.rectangle([x1, y1, x2, y2], fill=255)
        log.info(f"[mask] Rect: ({x1},{y1})→({x2},{y2})  {x2-x1}x{y2-y1}px")

    # Mask refinement: GrabCut (default) → SAM (opt-in) → rect fallback
    if getattr(args, "sam_refine", False):
        mask_img = _refine_with_sam(image_path, bboxes, mask_img,
                                    getattr(args, "sam_checkpoint", ""),
                                    padding=padding)
    elif not getattr(args, "no_grabcut", False):
        mask_img = _refine_with_grabcut(image_path, bboxes, mask_img, padding=padding)

    # Save mask next to input image
    safe_obj  = object_name.replace(" ", "_")
    mask_path = image_path.parent / f"{image_path.stem}_mask_{safe_obj}.png"
    mask_img.save(str(mask_path))
    log.info(f"[mask] Saved → {mask_path}")

    # Patch args
    args.mask = str(mask_path)
    if not args.prompt and "prompt" in data:
        args.prompt = data["prompt"]
        log.info(f"[mask] Auto-prompt: {args.prompt}")



def _refine_with_grabcut(image_path: Path, bboxes: list,
                         fallback: "PILImage.Image",
                         padding: int = 0) -> "PILImage.Image":
    """
    Refine a rect mask to object shape using GrabCut (cv2).
    Runs on CPU in <1s, zero new dependencies.
    Falls back to the rect mask if GrabCut produces a suspiciously small result
    (< 30% of the rect area — typical of low-contrast or failed segmentation).
    """
    import cv2
    import numpy as np
    from PIL import Image as PILImage

    img_np = np.array(PILImage.open(str(image_path)).convert("RGB"))
    H, W   = img_np.shape[:2]

    union = np.zeros((H, W), dtype=np.uint8)
    for bbox in bboxes:
        x1 = max(0,  int(bbox["x1"] * W) - padding)
        y1 = max(0,  int(bbox["y1"] * H) - padding)
        x2 = min(W,  int(bbox["x2"] * W) + padding)
        y2 = min(H,  int(bbox["y2"] * H) + padding)
        rect_w, rect_h = x2 - x1, y2 - y1
        if rect_w < 4 or rect_h < 4:
            union[y1:y2, x1:x2] = 1
            continue

        gc_mask  = np.zeros((H, W), np.uint8)
        bgd_mdl  = np.zeros((1, 65), np.float64)
        fgd_mdl  = np.zeros((1, 65), np.float64)
        try:
            cv2.grabCut(img_np, gc_mask, (x1, y1, rect_w, rect_h),
                        bgd_mdl, fgd_mdl, 5, cv2.GC_INIT_WITH_RECT)
        except cv2.error as e:
            log.warning(f"[GrabCut] Failed ({e}) — using rect mask for this instance")
            union[y1:y2, x1:x2] = 1
            continue

        fg = np.where((gc_mask == cv2.GC_FGD) | (gc_mask == cv2.GC_PR_FGD), 1, 0).astype(np.uint8)

        # Sanity check: GrabCut sometimes returns near-empty masks on low-contrast scenes
        rect_area = rect_w * rect_h
        fg_area   = int(fg.sum())
        coverage  = fg_area / max(rect_area, 1)
        log.info(f"[GrabCut] bbox=({x1},{y1})→({x2},{y2})  fg={fg_area}px  coverage={coverage:.1%}")

        if coverage < 0.10:
            log.warning(f"[GrabCut] Coverage {coverage:.1%} too low — falling back to rect for this instance")
            union[y1:y2, x1:x2] = 1
        else:
            union = np.clip(union + fg, 0, 1)

    return PILImage.fromarray((union * 255).astype(np.uint8), mode="L")


def _refine_with_sam(image_path: Path, bboxes: list,
                     fallback: "PILImage.Image", checkpoint: str,
                     padding: int = 0) -> "PILImage.Image":
    """
    Run SAM on every bbox instance and return the union mask (white = object).
    bboxes: list of dicts with normalised x1/y1/x2/y2 keys.
    padding: pixels to dilate the SAM mask — captures thin legs/edges and
             grows on retries so the mask expands meaningfully.
    """
    try:
        import torch, numpy as np
        import cv2
        from segment_anything import sam_model_registry, SamPredictor
        from PIL import Image as PILImage
    except ImportError:
        log.warning("segment-anything not installed — using rect mask. "
                    "pip install segment-anything torch torchvision")
        return fallback

    ckpt = Path(checkpoint)
    if not ckpt.exists():
        log.warning(f"SAM checkpoint not found: {ckpt} — using rect mask.")
        return fallback

    # Infer model type from checkpoint filename (vit_h / vit_l / vit_b)
    name = ckpt.stem.lower()
    if "vit_h" in name:
        model_type = "vit_h"
    elif "vit_l" in name:
        model_type = "vit_l"
    else:
        model_type = "vit_b"

    device = "cuda" if torch.cuda.is_available() else "cpu"
    log.info(f"[SAM] Loading {model_type} on {device}...")
    sam  = sam_model_registry[model_type](checkpoint=str(ckpt))
    sam.to(device)
    pred = SamPredictor(sam)

    img_np = np.array(PILImage.open(str(image_path)).convert("RGB"))
    h, w   = img_np.shape[:2]
    pred.set_image(img_np)

    union = np.zeros((h, w), dtype=bool)
    for bbox in bboxes:
        cx  = int(((bbox["x1"] + bbox["x2"]) / 2) * w)
        cy  = int(((bbox["y1"] + bbox["y2"]) / 2) * h)
        bx1 = int(bbox["x1"] * w)
        by1 = int(bbox["y1"] * h)
        bx2 = int(bbox["x2"] * w)
        by2 = int(bbox["y2"] * h)

        masks, scores, _ = pred.predict(
            point_coords=np.array([[cx, cy]]),
            point_labels=np.array([1]),
            box=np.array([bx1, by1, bx2, by2]),
            multimask_output=True,
        )
        best = masks[int(np.argmax(scores))]
        log.info(f"[SAM] bbox=({bx1},{by1})→({bx2},{by2})  score={scores.max():.3f}  coverage={best.sum()}px")
        union |= best

    del sam, pred
    if device == "cuda":
        torch.cuda.empty_cache()

    # Dilate by padding — covers thin elements (legs, edges) and grows on retries
    if padding > 0:
        k = padding * 2 + 1
        kernel = np.ones((k, k), np.uint8)
        union  = cv2.dilate(union.astype(np.uint8), kernel).astype(bool)
        log.info(f"[SAM] After dilation (+{padding}px): coverage={union.sum()}px")

    return PILImage.fromarray((union * 255).astype("uint8"), mode="L")


