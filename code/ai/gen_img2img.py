# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = build_parser()
    args   = parser.parse_args()

    if args.verbose:
        logging.getLogger().setLevel(logging.DEBUG)

    if args.mode in SHOULD_NOT_USE:
        log.error(f"[{args.mode}] SHOULD NOT USE: {SHOULD_NOT_USE[args.mode]}")
        sys.exit(2)

    engine_tag = f" | engine={args.engine}" if args.mode == "inpaint" else ""
    log.info(f"Mode: {args.mode}{engine_tag} | Device: {args.device} | FP16: {not args.no_fp16}")
    log.info(f"VRAM profile: {cfg.VRAM_PROFILES[args.mode]}")

    pipe = load_pipe_for_mode(args.mode, args)

    try:
        if args.manifest:
            # Batch mode: resolve mask once then run all items
            if args.mode == "inpaint":
                resolve_mask_and_prompt(args)
            _validate_args(args.mode, args)
            run_batch(args.mode, pipe, args)

        elif args.mode == "inpaint" and not args.manifest:
            # Single inpaint with optional auto-verify + retry loop.
            # Save original object name so mask can be regenerated on retry.
            _orig_mask        = args.mask          # may be object name or file path
            _is_auto_mask     = _orig_mask and not Path(_orig_mask).exists()
            _max_retries      = getattr(args, "max_retries", 3)
            _verify_enabled   = _is_auto_mask and _max_retries > 0

            resolve_mask_and_prompt(args)
            _validate_args(args.mode, args)
            run_single(args.mode, pipe, args)

            if _verify_enabled:
                out_path    = args.output or _default_output(args, args.mode)
                object_name = _orig_mask
                claude_bin  = getattr(args, "claude_bin", "claude")

                for attempt in range(1, _max_retries + 1):
                    v = verify_inpaint(out_path, object_name, claude_bin)

                    if not v.get("still_present", False):
                        log.info(
                            f"[verify] '{object_name}' not detected — inpaint accepted "
                            f"(confidence={v.get('confidence', '?')})."
                        )
                        break

                    # Object still visible — increase padding and retry
                    increase = max(int(v.get("padding_increase", 50)), 20)
                    log.warning(
                        f"[verify] Attempt {attempt}/{_max_retries}: '{object_name}' still present "
                        f"({v.get('reason', '')}).  "
                        f"Increasing mask_padding by {increase}px and retrying..."
                    )
                    args.mask         = object_name          # restore for re-generation
                    args.mask_padding = getattr(args, "mask_padding", 100) + increase

                    # Delete old mask file so it is regenerated with new padding
                    old_mask_path = Path(args.input).parent / \
                        f"{Path(args.input).stem}_mask_{object_name.replace(' ', '_')}.png"
                    if old_mask_path.exists():
                        old_mask_path.unlink()

                    resolve_mask_and_prompt(args)
                    run_single(args.mode, pipe, args)
                else:
                    log.warning(
                        f"[verify] Max retries ({_max_retries}) reached. "
                        "Keeping last result. Consider running with --mask-padding manually."
                    )

        else:
            _validate_args(args.mode, args)
            run_single(args.mode, pipe, args)

    finally:
        unload_pipe(pipe)
        log.info("Pipeline complete.")


if __name__ == "__main__":
    main()
