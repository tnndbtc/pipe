_CLAUDE_VERIFY_PROMPT = (
    "Look at this image: {image_path}\n\n"
    "An AI inpainting tool tried to erase the {object} from this image.\n"
    "Check carefully for ANY part of the {object} still present: the full body, "
    "legs, base, feet, arms, back, seat, or any partial remains — even a single leg "
    "or a small fragment counts as still_present=true.\n"
    "Only count real 3D physical objects, NOT wall paintings, murals, or artwork.\n"
    "Return ONLY a single line of JSON with no explanation:\n"
    '{{"still_present": <true|false>, "confidence": <0.0-1.0>, '
    '"padding_increase": <extra pixels to add to mask_padding to cover the remaining part, 0 if not present>, '
    '"reason": "<one short sentence describing exactly what part is still visible or why it is gone>"}}\n'
    "If still_present=true, set padding_increase large enough to cover the remaining part "
    "(e.g. if only legs remain outside current mask, 100-200px is typical)."
)


def verify_inpaint(output_path: str, object_name: str, claude_bin: str) -> dict:
    """
    Call claude -p to check whether the object is still visible in the output image.
    Returns a dict with keys: still_present, confidence, padding_increase, reason.
    Returns {"still_present": False} on any failure (assume success).
    """
    prompt = _CLAUDE_VERIFY_PROMPT.format(
        image_path=str(Path(output_path).resolve()),
        object=object_name,
    )
    log.info(f"[verify] Asking claude to check if '{object_name}' is still present...")
    try:
        result = subprocess.run(
            [claude_bin, "-p", prompt],
            capture_output=True, text=True, timeout=60,
        )
    except (FileNotFoundError, subprocess.TimeoutExpired) as e:
        log.warning(f"[verify] claude -p failed ({e}); assuming success.")
        return {"still_present": False}

    if result.returncode != 0:
        log.warning(f"[verify] claude -p exited {result.returncode}; assuming success.")
        return {"still_present": False}

    stdout = result.stdout.strip()
    log.info(f"[verify] Claude response: {stdout}")

    data = _extract_last_json(stdout)  # last block: Claude sometimes self-corrects in a second block
    if not data:
        log.warning("[verify] No JSON in claude response; assuming success.")
        return {"still_present": False}

    return data


