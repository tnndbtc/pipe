def load_pipe_for_mode(mode: str, args):
    from img2img.model_loader import (
        load_flux_img2img, load_flux_fill, load_flux_controlnet,
        load_flux_redux_prior, load_flux_dev_img2img,
        load_sd_upscaler, load_depth_model,
        load_sdxl_inpaint, load_lama,
    )
    no_fp16 = getattr(args, "no_fp16", False)

    if mode == "composite":
        pipe = {"depth": load_depth_model() if args.depth_scale else None}
        if getattr(args, "blend_strength", None) and args.blend_strength > 0.0:
            pipe["flux"] = load_flux_img2img()
        return pipe

    elif mode == "inpaint":
        engine = getattr(args, "engine", "flux")
        if engine == "lama":
            return load_lama()
        elif engine == "sdxl":
            return load_sdxl_inpaint(no_fp16)
        elif engine == "opencv":
            return None          # OpenCV needs no model
        else:                    # "flux" (default)
            return load_flux_fill()

    elif mode == "outpaint":
        return load_flux_fill()

    elif mode == "style":
        return load_flux_img2img()

    elif mode == "pose":
        return load_flux_controlnet(cfg.MODEL_IDS["flux_controlnet_pose"])

    elif mode == "depth":
        return {"flux_controlnet": load_flux_controlnet(cfg.MODEL_IDS["flux_controlnet_depth"]),
                "depth":           load_depth_model()}

    elif mode == "canny":
        return load_flux_controlnet(cfg.MODEL_IDS["flux_controlnet_canny"])

    elif mode == "upscale":
        return load_sd_upscaler(no_fp16)

    elif mode == "bg_replace":
        pipe = {}
        if getattr(args, "blend_strength", None) and args.blend_strength > 0.0:
            pipe["flux"] = load_flux_img2img()
        return pipe

    elif mode == "ip_adapter":
        return {"prior": load_flux_redux_prior(), "flux": load_flux_dev_img2img()}

    elif mode in ("relight", "color_grade"):
        return None

    raise ValueError(f"Unknown mode: {mode}")


def unload_pipe(pipe) -> None:
    from img2img.model_loader import unload_model
    if isinstance(pipe, dict):
        for v in pipe.values():
            unload_model(v)
    else:
        unload_model(pipe)


